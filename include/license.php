<h2>Лицензионность</h2>
<h3>Основные признаки лицензионности программ "1С"</h3>
<ul>
    <li><b>Наличие регистрационной анкеты с названием программного продукта и регистрационным номером</b></li>
    <li><b>Наличие ключа защиты (аппаратного, или программного)</b></li>
    <li><b>Наличие установочного диска с названием и регистрационным номером</b></li>
    <li><b>Наличие коробки «1С» с документацией ( от 2 до 12 книг с регистрационными номерами или регистрационным номером)</b></li>
</ul>