<h1>Выгрузка 1С-Битрикс</h1>
<p>
	 Не все пользователи программы знают, что уже прошли те времена, когда приходилось вручную переносить все данные из таблиц в автоматизированное решение. Сегодня все это можно сделать проще и без потери информации. Выгрузка 1с-битрикс доступна, если приобрести специальный модуль для импорта. Его функционал позволяет:  <br>
</p>
<ul>
	<li><p>формировать значения информационных ячеек на базе табличных показателей </p></li>
	<li><p>дополнительно оформлять в течение загрузки данных в 1С-Битрикс параметры свойств и задавать характеристики</p></li>
	<li><p>грузить информацию выборочно по параметрам </p></li>
	<li><p>сравнивать вновь полученные значения с учетом номенклатуры и цен </p></li>
</ul>
 <br>
<p>
	 Это загрузка данных в 1C-Битрикс, которая позволяет решить следующие задачи: <br>
</p>
<ul>
	<li><p>актуальное обновление ценовых показателей </p></li>
	<li><p>изменение количественных характеристик по товарным группам </p></li>
	<li><p>полное изменение описаний к номенклатурным позициям </p></li>
	<li><p>простое добавление новых номенклатурных единиц </p></li>
	<li><p>•	выгрузка заказов в 1С-Битрикс с формированием новых категорий </p></li>
</ul>
